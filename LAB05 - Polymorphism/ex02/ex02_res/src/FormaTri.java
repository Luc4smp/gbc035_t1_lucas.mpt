public class FormaTri extends Forma{
    private double area;
    private double volume;
    FormaTri(){
        area =0;
        volume = 0;
    }
    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public double getVolume() {
        return volume;
    }

    public void setVolume(double volume) {
        this.volume = volume;
    }
}
